/**
 * Regenerates lib/latvia-geo.ts — the accurate SVG geometry for Latvia's five
 * planning regions used by components/latvia-map.tsx.
 *
 * Source: geoBoundaries gbOpen LVA ADM1 (data © Geoportal of Latvia, CC BY 4.0)
 * — Latvia's 43 administrative territories. We map each territory to its planning
 * region, dissolve the shared internal borders (edge cancellation), project to an
 * SVG viewBox (equirectangular with cos(lat) correction) and simplify each region
 * outline with Douglas–Peucker.
 *
 * Run:  node scripts/gen-latvia-map.mjs
 * Requires network access (downloads the GeoJSON once).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.join(__dirname, "..", "lib", "latvia-geo.ts");
const GEOJSON_URL =
  "https://github.com/wmgeolab/geoBoundaries/raw/9469f09/releaseData/gbOpen/LVA/ADM1/geoBoundaries-LVA-ADM1_simplified.geojson";

// 43 administrative territories → 5 planning regions (official assignment).
const REGION_OF = {
  "Dienvidkurzemes novads": "kurzeme", "Kuldīgas novads": "kurzeme", "Saldus novads": "kurzeme",
  "Talsu novads": "kurzeme", "Ventspils novads": "kurzeme", "Ventspils": "kurzeme", "Liepājas": "kurzeme",
  "Rīga": "pieriga", "Jūrmalas": "pieriga", "Ādažu novads": "pieriga", "Limbažu novads": "pieriga",
  "Mārupes novads": "pieriga", "Ogres novads": "pieriga", "Olaines novads": "pieriga",
  "Ropažu novads": "pieriga", "Salaspils novads": "pieriga", "Saulkrastu novads": "pieriga",
  "Siguldas novads": "pieriga", "Tukuma novads": "pieriga", "Ķekavas novads": "pieriga",
  "Alūksnes novads": "vidzeme", "Cēsu novads": "vidzeme", "Gulbenes novads": "vidzeme",
  "Madonas novads": "vidzeme", "Smiltenes novads": "vidzeme", "Valkas novads": "vidzeme",
  "Valmieras novads": "vidzeme",
  "Aizkraukles novads": "zemgale", "Bauskas novads": "zemgale", "Dobeles novads": "zemgale",
  "Jelgavas novads": "zemgale", "Jelgavas": "zemgale", "Jēkabpils novads": "zemgale",
  "Augšdaugavas novads": "latgale", "Balvu novads": "latgale", "Daugavpils": "latgale",
  "Krāslavas novads": "latgale", "Ludzas novads": "latgale", "Līvānu novads": "latgale",
  "Preiļu novads": "latgale", "Rēzeknes": "latgale", "Rēzeknes novads": "latgale",
  "Varakļānu novads": "latgale",
};
const ORDER = ["kurzeme", "pieriga", "zemgale", "vidzeme", "latgale"];
const W = 1000, PAD = 8;

async function main() {
  const res = await fetch(GEOJSON_URL);
  if (!res.ok) throw new Error(`Download failed: ${res.status}`);
  const g = await res.json();

  const unmapped = g.features.map((f) => f.properties.shapeName).filter((n) => !REGION_OF[n]);
  if (unmapped.length) throw new Error("Unmapped territories: " + unmapped.join(", "));

  let lonMin = 999, lonMax = -999, latMin = 999, latMax = -999;
  for (const f of g.features) for (const ring of f.geometry.coordinates) for (const [x, y] of ring) {
    if (x < lonMin) lonMin = x; if (x > lonMax) lonMax = x;
    if (y < latMin) latMin = y; if (y > latMax) latMax = y;
  }
  const kx = Math.cos(((latMin + latMax) / 2 * Math.PI) / 180);
  const scale = (W - 2 * PAD) / ((lonMax - lonMin) * kx);
  const H = Math.round((latMax - latMin) * scale + 2 * PAD);
  const project = ([lon, lat]) => [
    Math.round(PAD + (lon - lonMin) * kx * scale),
    Math.round(PAD + (latMax - lat) * scale),
  ];

  const byRegion = {};
  for (const f of g.features) {
    const rid = REGION_OF[f.properties.shapeName];
    (byRegion[rid] ??= []).push(...f.geometry.coordinates.map((r) => r.map(project)));
  }

  const regions = ORDER.map((id) => {
    const loops = dissolve(byRegion[id]);
    let cxS = 0, cyS = 0, aS = 0;
    const paths = loops
      .map((loop) => {
        let pts = loop.filter((p, i) => i === 0 || p[0] !== loop[i - 1][0] || p[1] !== loop[i - 1][1]);
        pts = dp(pts, 1.2);
        const c = ringCentroid(pts);
        cxS += c.cx * c.area; cyS += c.cy * c.area; aS += c.area;
        return { d: "M" + pts.map(([x, y]) => `${x} ${y}`).join("L") + "Z", area: c.area };
      })
      .sort((a, b) => b.area - a.area);
    return { id, label: { x: +(cxS / aS).toFixed(1), y: +(cyS / aS).toFixed(1) }, paths: paths.map((p) => p.d) };
  });

  const ts = `// AUTO-GENERATED by scripts/gen-latvia-map.mjs — do not edit by hand.
// Source: geoBoundaries gbOpen LVA ADM1 (data © Geoportal of Latvia, CC BY 4.0),
// 43 administrative territories dissolved into the 5 planning regions, projected
// to an SVG viewBox and simplified (Douglas–Peucker). Clean single region outlines.
import type { RegionId } from "@/lib/regions";
export interface RegionGeo { id: RegionId; label: { x: number; y: number }; paths: string[]; }
export const LATVIA_VIEWBOX = "0 0 ${W} ${H}";
export const LATVIA_GEO: RegionGeo[] = ${JSON.stringify(regions)};
`;
  fs.writeFileSync(OUT, ts);
  console.log("Wrote", OUT);
  regions.forEach((r) => console.log(`  ${r.id}: ${r.paths.length} loop(s), label ${r.label.x},${r.label.y}`));
}

// --- dissolve municipality rings of one region into outer boundary loop(s) ---
function dissolve(rings) {
  const edges = new Map();
  const key = (a, b) => `${a[0]},${a[1]}|${b[0]},${b[1]}`;
  for (const ring of rings) for (let i = 0; i < ring.length - 1; i++) {
    const a = ring[i], b = ring[i + 1];
    if (a[0] === b[0] && a[1] === b[1]) continue;
    const rev = key(b, a);
    if (edges.has(rev)) edges.delete(rev); // shared internal edge cancels
    else edges.set(key(a, b), [a, b]);
  }
  const adj = new Map();
  for (const [, [a, b]] of edges) (adj.get(`${a[0]},${a[1]}`) ?? adj.set(`${a[0]},${a[1]}`, []).get(`${a[0]},${a[1]}`)).push(b);
  const loops = [], used = new Set();
  for (const [, [a]] of edges) {
    if (used.has(`${a[0]},${a[1]}`)) continue;
    const loop = [a]; let cur = a, guard = 0;
    while (guard++ < 100000) {
      const k = `${cur[0]},${cur[1]}`;
      const nexts = adj.get(k);
      if (!nexts || !nexts.length) break;
      const nxt = nexts.shift(); used.add(k); loop.push(nxt); cur = nxt;
      if (cur[0] === a[0] && cur[1] === a[1]) break;
    }
    if (loop.length > 3) loops.push(loop);
  }
  return loops;
}

// --- Douglas–Peucker line simplification ---
function dp(pts, eps) {
  if (pts.length < 3) return pts;
  let md = 0, idx = 0;
  for (let i = 1; i < pts.length - 1; i++) {
    const d = perp(pts[i], pts[0], pts[pts.length - 1]);
    if (d > md) { md = d; idx = i; }
  }
  return md > eps ? dp(pts.slice(0, idx + 1), eps).slice(0, -1).concat(dp(pts.slice(idx), eps)) : [pts[0], pts[pts.length - 1]];
}
function perp(p, a, b) {
  const [px, py] = p, [ax, ay] = a, [bx, by] = b;
  const dx = bx - ax, dy = by - ay, l2 = dx * dx + dy * dy;
  if (!l2) return Math.hypot(px - ax, py - ay);
  let t = ((px - ax) * dx + (py - ay) * dy) / l2;
  t = Math.max(0, Math.min(1, t));
  return Math.hypot(px - (ax + t * dx), py - (ay + t * dy));
}
function ringCentroid(r) {
  let a = 0, cx = 0, cy = 0;
  for (let i = 0; i < r.length - 1; i++) {
    const [x0, y0] = r[i], [x1, y1] = r[i + 1], c = x0 * y1 - x1 * y0;
    a += c; cx += (x0 + x1) * c; cy += (y0 + y1) * c;
  }
  a *= 0.5;
  return Math.abs(a) < 1e-6 ? { area: 0, cx: r[0][0], cy: r[0][1] } : { area: Math.abs(a), cx: cx / (6 * a), cy: cy / (6 * a) };
}

main().catch((e) => { console.error(e); process.exit(1); });
